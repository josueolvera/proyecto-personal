import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author josueolvera
 */
@Entity
@DynamicUpdate
@Table(name = "C_OPERATIONS")
@JsonIdentityInfo(generator = ObjectIdGenerators.IntSequenceGenerator.class, property = "_id")
public class COperations implements Serializable {

    private static final long serialVersionUID = 1L;

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "ID_OPERATION")
    @JsonView(JsonViews.Root.class)
    private Integer idOperation;

    @Size(max = 50)
    @Column(name = "OPERACION_SQL")
    @JsonView(JsonViews.Root.class)
    private String operacionSql;

    @Size(max = 50)
    @Column(name = "DESCRIPCTION")
    @JsonView(JsonViews.Root.class)
    private String descripction;

    public COperations() {
    }

    public COperations(Integer idOperation) {
        this.idOperation = idOperation;
    }

    public Integer getIdOperation() {
        return idOperation;
    }

    public void setIdOperation(Integer idOperation) {
        this.idOperation = idOperation;
    }

    public String getOperacionSql() {
        return operacionSql;
    }

    public void setOperacionSql(String operacionSql) {
        this.operacionSql = operacionSql;
    }

    public String getDescripction() {
        return descripction;
    }

    public void setDescripction(String descripction) {
        this.descripction = descripction;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (idOperation != null ? idOperation.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof COperations)) {
            return false;
        }
        COperations other = (COperations) object;
        if ((this.idOperation == null && other.idOperation != null) || (this.idOperation != null && !this.idOperation.equals(other.idOperation))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "mx.bidg.model.COperations[ idOperation=" + idOperation + " ]";
    }
    
}
